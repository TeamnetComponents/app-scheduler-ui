'use strict';

schedulerServices
    .factory('Task', function ($resource) {
        return $resource('app/rest/task/:id', {}, {
            'query': { method: 'GET', isArray: true},
            'get': {
                method: 'GET',
                transformResponse: function (data) {
                    data = angular.fromJson(data);
                    return data;
                }
            }
        });
    });
